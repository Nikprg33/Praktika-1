def sum_negatives_between_min_max(arr):
    """
    Находит сумму отрицательных элементов, расположенных между максимальным и минимальным элементами массива.
    
    Args:
        arr: список чисел (одномерный массив)
    
    Returns:
        int/float: сумма отрицательных элементов между минимумом и максимумом
    """
    # Проверка на пустой массив
    if not arr:
        return 0
    
    # Находим индексы минимального и максимального элементов
    min_index = arr.index(min(arr))
    max_index = arr.index(max(arr))
    
    # Определяем границы диапазона (включая оба конца)
    start = min(min_index, max_index)
    end = max(min_index, max_index)
    
    # Суммируем отрицательные элементы в диапазоне между минимумом и максимумом
    # Важно: не включаем сами минимальный и максимальный элементы
    total = 0
    for i in range(start + 1, end):
        if arr[i] < 0:
            total += arr[i]
    
    return total

# Примеры использования:
if __name__ == "__main__":
    # Тестовые примеры
    test_cases = [
        [1, -2, 3, -4, 5],           # min=1(0), max=5(4) -> между: [-2, 3, -4] -> отрицательные: -2, -4 -> сумма: -6
        [-5, 2, -1, 8, -3],          # min=-5(0), max=8(3) -> между: [2, -1] -> отрицательные: -1 -> сумма: -1
        [3, 1, 4, 1, 5],             # min=1(1 или 3), max=5(4) -> между индексами 1 и 4: [4, 1] -> нет отрицательных -> 0
        [-1, -2, -3],                # min=-3(2), max=-1(0) -> между: [-2] -> сумма: -2
        [5],                         # только один элемент -> между нет элементов -> 0
        [],                          # пустой массив -> 0
        [2, -5, 1, -3, 4, -1, 6]    # min=-5(1), max=6(6) -> между: [1, -3, 4, -1] -> отрицательные: -3, -1 -> сумма: -4
    ]
    
    for i, test in enumerate(test_cases):
        result = sum_negatives_between_min_max(test)
        print(f"Тест {i+1}: {test} -> сумма отрицательных между min и max: {result}")